    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        try {
          int lapangan = 0,kostum = 0,sepatu = 0,tambahan;
          if(lblLapangan.getText() != "-"){
              lapangan = Integer.parseInt(lblHargaLapangan.getText()) * Integer.parseInt(lblLama1.getText());
          }
            if (ckSepatu.isSelected()) {
                sepatu = 50000;
            }
            if (ckKostum.isSelected()) {
                kostum = 45000;
            }
          tambahan = (sepatu + kostum) * Integer.parseInt(lblLama1.getText());
          total = lapangan + tambahan;
          lblTotal.setText(Integer.toString(total));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        
        
    }//GEN-LAST:event_btnTotalActionPerformed

