    private void cmbJenisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbJenisActionPerformed
        String jnsLapangan = (String) cmbJenis.getSelectedItem();
        if(rdnIndoor.isSelected()){
            switch(jnsLapangan){
                case "REGULER" : 
                    lblLapangan.setText("INDOOR - REGULER");
                    lblHargaLapangan.setText("300000");
                    break;
                case "MATRAS" :
                    lblLapangan.setText("INDOOR - MATRAS");
                    lblHargaLapangan.setText("250000");
                    break;
                case "RUMPUT" :
                    lblLapangan.setText("INDOOR - RUMPUT");
                    lblHargaLapangan.setText("200000");
                    break;
                default:
                    lblLapangan.setText("-");
                    lblHargaLapangan.setText("-");
                    break;
        }
        } else if (rdnOutdoor.isSelected()){
            switch(jnsLapangan){
                case "REGULER" : 
                    lblLapangan.setText("OUTDOOR - REGULER");
                    lblHargaLapangan.setText("250000");
                    break;
                case "MATRAS" :
                    lblLapangan.setText("OUTDOOR - MATRAS");
                    lblHargaLapangan.setText("200000");
                    break;
                case "RUMPUT" :
                    lblLapangan.setText("OUTDOOR - RUMPUT");
                    lblHargaLapangan.setText("150000");
                    break;
                default:
                    lblLapangan.setText("-");
                    lblHargaLapangan.setText("-");
                    break;
            }
                
        }
       
    }//GEN-LAST:event_cmbJenisActionPerformed

